package online.selfexpansion.commitment.model;

public class WebSiteInfo {
    private String ws_name;
    private String ws_desc;
    private String ws_copyright;

    public String getWs_name() {
        return ws_name;
    }

    public String getWs_desc() {
        return ws_desc;
    }

    public String getWs_copyright() {
        return ws_copyright;
    }

    public void setWs_name(String ws_name) {
        this.ws_name = ws_name;
    }

    public void setWs_desc(String ws_desc) {
        this.ws_desc = ws_desc;
    }

    public void setWs_copyright(String ws_copyright) {
        this.ws_copyright = ws_copyright;
    }
}
